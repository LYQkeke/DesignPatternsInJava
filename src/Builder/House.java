public class House {

    private String foor;

    private String ceil;

    private String wall;

    public String getFoor() {
        return foor;
    }

    public void setFoor(String foor) {
        this.foor = foor;
    }

    public String getCeil() {
        return ceil;
    }

    public void setCeil(String ceil) {
        this.ceil = ceil;
    }

    public String getWall() {
        return wall;
    }

    public void setWall(String wall) {
        this.wall = wall;
    }
}
